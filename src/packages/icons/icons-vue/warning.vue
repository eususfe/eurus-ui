export default defineComponent({
  name: 'Warning',
  props: {
    size: {
      type: [Number, String],
    },
    rotate: Number,
    spin: Boolean
  },
  setup(props) {
    const prefixCls = getPrefixCls('icon');
    const cls = computed(() => [prefixCls, `${prefixCls}-warning`, { [`${prefixCls}-spin`]: props.spin }]);
